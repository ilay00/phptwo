abstract class C_Base extends C_Controller
{
	protected $title;		// ????????? ????????
	protected $content;		// ?????????? ????????

	
	
	protected function before()
	{
		$this->title = 'PHPSHOP';
		$this->content = '';
	}
	
	//
	// ????????? ???????? ????????
	//	
	public function render()
	{
		$vars = array('title' => $this->title, 'content' => $this->content);	
		$page = $this->Template('v/v_main.php', $vars);				
		echo $page;
	}	
}
